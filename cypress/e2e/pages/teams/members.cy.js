const API_URL = Cypress.env('API_URL');

const resolveFirstOrgId = (token) => {
  return cy
    .request({
      method: 'GET',
      url: `${API_URL}/fn-execute/organization`,
      headers: { Authorization: `IDENTITY ${token}` },
      qs: { sort: JSON.stringify({ _id: -1 }) },
      failOnStatusCode: false,
    })
    .then((res) => {
      const list = Array.isArray(res.body) ? res.body : res.body?.data || [];
      const first = list.find((o) => !o?.deleted_at) || list[0];
      if (!first?._id) {
        throw new Error('[teams/members spec] resolveFirstOrgId: no organization on this account');
      }
      return first._id;
    });
};

const resolveOrCreateTeamId = (token, orgId) => {
  return cy
    .request({
      method: 'GET',
      url: `${API_URL}/fn-execute/organization/${orgId}/team`,
      headers: { Authorization: `IDENTITY ${token}` },
      qs: { filter: JSON.stringify({ deleted_at: { $eq: null } }), sort: JSON.stringify({ _id: -1 }) },
      failOnStatusCode: false,
    })
    .then((res) => {
      const list = Array.isArray(res.body) ? res.body : res.body?.data || [];
      const first = list.find((t) => !t?.deleted_at) || list[0];
      if (first?._id) return first._id;

      const name = `cy-team-members-${Date.now()}`;
      return cy
        .request({
          method: 'POST',
          url: `${API_URL}/fn-execute/organization/${orgId}/team`,
          headers: { Authorization: `IDENTITY ${token}` },
          body: { name, description: 'Cypress autogenerated team for members spec', organization: orgId },
          failOnStatusCode: false,
        })
        .then((createRes) => {
          const id = createRes.body?._id || createRes.body?.data?._id;
          if (!id) {
            throw new Error('[teams/members spec] resolveOrCreateTeamId: failed to create fallback team');
          }
          return id;
        });
    });
};

const seedTeamContext = () => {
  // cy.login() seeds cy.session() so editor pages bootstrap with the token
  // in localStorage. cy.getAuthToken() POSTs /login directly to obtain a
  // token usable by cy.request() — this is decoupled from window.localStorage
  // which is unreliable on cache-hit cy.session() restores (about:blank).
  cy.login();
  cy.getAuthToken().then((token) => {
    resolveFirstOrgId(token).then((orgId) => {
      cy.wrap(orgId).as('orgId');
      resolveOrCreateTeamId(token, orgId).then((teamId) => {
        cy.wrap(teamId).as('teamId');
      });
    });
  });
};

describe('Teams Members — page mount', () => {
  beforeEach(function () {
    seedTeamContext();
    cy.get('@teamId').then((teamId) => {
      cy.get('@orgId').then((orgId) => {
        cy.intercept('GET', `**/api/fn-execute/organization/${orgId}/team/${teamId}**`).as('getTeam');
        cy.visit(`/organizations/${orgId}/teams/${teamId}/members`);
        cy.wait('@getTeam', { timeout: 20000 });
      });
    });
  });

  it('should land on the /organizations/:orgId/teams/:teamId/members URL after navigation', function () {
    cy.location('pathname').should('eq', `/organizations/${this.orgId}/teams/${this.teamId}/members`);
  });

  it('should render the members prefab root so the team members body is mounted', () => {
    // ScopeView is the actual root of the prefab; spec accepts any of its
    // recognisable structural classes until a stable data-cy is added.
    cy.get(
      '[data-cy="members-prefab-root"], [class*="scopeView"], [class*="ScopeView"]',
      { timeout: 20000 }
    )
      .first()
      .should('exist');
  });

  it('should expose the invite member action so the page CTA is reachable', () => {
    // Fallback: locate any button whose label text matches the team-scope CTA
    // ("Invite member"). Will be replaced once data-cy="members-invite-btn" lands.
    cy.get('button', { timeout: 20000 })
      .filter((_, el) => /invite\s*member/i.test(el.innerText || ''))
      .first()
      .should('exist');
  });
});

